package by.zastr.xml.exception;

public class XmlDepositException extends Exception {
	public XmlDepositException() {
    }

    public XmlDepositException(String message) {
        super(message);
    }

    public XmlDepositException(String message, Throwable cause) {
        super(message, cause);
    }

    public XmlDepositException(Throwable cause) {
        super(cause);
    }

}
